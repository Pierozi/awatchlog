#![crate_type = "bin"]

extern crate getopts;
extern crate awatchlog;

use getopts::Options;
use std::env;

fn main() {
    let args: Vec<String> = env::args().collect();
    let program = args[0].clone();

    let mut opts = Options::new();
    opts.optopt(
        "c",
        "configuration.rs",
        "configuration file in TOML (default: /etc/awatchlog/configuration.rs.toml)",
        "Configuration"
    );
    opts.optopt(
        "",
        "credentials",
        "credentials file in TOML",
        "Credentials"
    );
    opts.optflag("h", "help", "print this help menu");

    let matches = match opts.parse(&args[1..]) {
        Ok(m) => { m }
        Err(f) => { panic!(f.to_string()) }
    };

    if matches.opt_present("h") {
        print_usage(&program, opts);
        return;
    }

    let config_file = matches.opt_str("c");
    let credentials_file = matches.opt_str("credentials");

    awatchlog::run(config_file, credentials_file);
}

fn print_usage(program: &str, opts: Options) {
    let brief = format!("Usage: {} [options]", program);
    print!("{}", opts.usage(&brief));
}
